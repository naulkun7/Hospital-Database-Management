  private void ShowTableActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_ShowTableActionPerformed

    DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
    model.setRowCount(0);

    try {

      Class.forName("com.mysql.cj.jdbc.Driver");
      Connection connection = DriverManager.getConnection(
          "jdbc:mysql://localhost:3306/HDSNEW?useSSL=false&allowPublicKeyRetrieval=true&serverTimezone=UTC",
          "root", "troixanhmaytrang0412");

      Statement st = connection.createStatement();
      String sql = "select *from Doctor";

      ResultSet rs = st.executeQuery(sql);

      while (rs.next()) {

        String D_Id = String.valueOf(rs.getString("DoctorID"));
        String D_name = String.valueOf(rs.getString("Doctor_Name"));
        String Phone_No = String.valueOf(rs.getString("Doctor_Phone"));
        String Department = String.valueOf(rs.getString("Department"));

        String tbData[] = { D_Id, D_name, Phone_No, Department };

        DefaultTableModel tblModel = (DefaultTableModel) jTable3.getModel();

        tblModel.addRow(tbData);

      }

    } catch (Exception e) {

      e.printStackTrace();

    }

  }// GEN-LAST:event_ShowTableActionPerformed

