    private void Show_TableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Show_TableActionPerformed

         
        DefaultTableModel model = (DefaultTableModel) jTable30.getModel();
            model.setRowCount(0);
        
          try{
              
             Class.forName("com.mysql.cj.jdbc.Driver");
            Connection connection = DriverManager.getConnection(
                    "jdbc:mysql://localhost:3306/HDSNEW?useSSL=false&allowPublicKeyRetrieval=true&serverTimezone=UTC", "root", "troixanhmaytrang0412");
   
   
 
       Statement st = connection.createStatement();
       String sql = "select Bill.BillID,Bill.PatientID,Patient.Patient_Name,Bill.Release_Date,Lab.Admission_Date,DATEDIFF(Bill.Release_Date, Lab.Admission_Date) AS No_Of_Days from Bill join Lab on Bill.PatientID = Lab.PatientID join Patient on Patient.PatientID = Lab.PatientID  join Lab_Report on Lab_Report.PatientID = patient.PatientID where Lab_Report.Patient_Category='In'";
       
       ResultSet rs = st.executeQuery(sql);
           
       while(rs.next()){
           
           String Bill_Id = String.valueOf(rs.getString("BillID"));
           String P_Id = String.valueOf(rs.getString("PatientID"));
           String P_Name= String.valueOf(rs.getString("Patient_Name"));           
           String R_Date = String.valueOf(rs.getString("Release_Date"));
           String A_Date =  String.valueOf(rs.getString("Admission_Date"));
           String Total_Days = String.valueOf(rs.getString("No_Of_Days"));
           
              
          
           String tbData[] = {Bill_Id ,P_Id,P_Name,R_Date,  A_Date,Total_Days};
           
           DefaultTableModel tblModel = (DefaultTableModel)jTable30.getModel();
           
           tblModel.addRow(tbData);
           
       }
       
       
        
        } catch (Exception e) {
            
            e.printStackTrace();
            
        }
        
                      
        
        
        
    }//GEN-LAST:event_Show_TableActionPerformed

