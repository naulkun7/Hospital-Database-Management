    private void ShowTableActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_ShowTableActionPerformed

        DefaultTableModel model = (DefaultTableModel) search_table1.getModel();
        model.setRowCount(0);
        try {

            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection connection = DriverManager.getConnection(
                    "jdbc:mysql://127.0.0.1:3306/HMSNEW?useSSL=false&allowPublicKeyRetrieval=true&serverTimezone=UTC",
                    "root", "hoangluan");
            Statement st = connection.createStatement();
            String sql = "SELECT patient.PatientID, patient.Patient_Name, patient.Age, patient.DoctorID, lab.Admission_Date, "
                    +
                    "DATEDIFF(bill.Release_Date, lab.Admission_Date) AS No_of_Days, " +
                    "(DATEDIFF(bill.Release_Date, lab.Admission_Date) * (lab.Amount + room.Room_Charge)) AS totalBill "
                    +
                    "FROM patient " +
                    "JOIN lab ON patient.PatientID = lab.PatientID " +
                    "JOIN bill ON patient.PatientID = bill.PatientID " +
                    "JOIN room ON patient.PatientID = room.PatientID";

            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                String P_Id = String.valueOf(rs.getInt("PatientID"));
                String P_name = String.valueOf(rs.getString("Patient_Name"));
                String No_of_Days = String.valueOf(rs.getInt("No_of_Days"));
                String totalBill = String.valueOf(rs.getInt("totalBill"));

                String tbData[] = { P_Id, P_name, No_of_Days, totalBill };

                DefaultTableModel tblModel = (DefaultTableModel) search_table1.getModel();
                tblModel.addRow(tbData);
            }

        } catch (Exception e) {

            e.printStackTrace();

        }

    }// GEN-LAST:event_ShowTableActionPerformed

